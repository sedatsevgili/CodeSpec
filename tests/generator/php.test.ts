describe("PHP Generator: statements", () => {
  it("should generate WHEN as if statement", () => {
    const mod = module({
      name: "Test",
      members: [
        action({
          name: "check",
          params: [
            parameter({ name: "x", paramType: primitiveType("Int") }),
          ],
          returnType: primitiveType("String"),
          body: [
            when({
              condition: "x > 0",
              body: [returnNode({ value: "positive" })],
              otherwise: [returnNode({ value: "negative" })],
            }),
          ],
        }),
      ],
    });
    const code = generatePhp(mod);
    expect(code).toContain("if ($x > 0) {");
    expect(code).toContain("} else {");
  });

  it("should generate MATCH as switch statement", () => {
    const mod = module({
      name: "Test",
      members: [
        action({
          name: "route",
          params: [
            parameter({ name: "role", paramType: primitiveType("String") }),
          ],
          returnType: primitiveType("Void"),
          body: [
            match({
              subject: "role",
              arms: [
                matchArm({
                  pattern: "admin",
                  body: [returnNode({ value: "panel" })],
                }),
                matchArm({
                  pattern: "_",
                  body: [returnNode({ value: "home" })],
                }),
              ],
            }),
          ],
        }),
      ],
    });
    const code = generatePhp(mod);
    expect(code).toContain("switch ($role) {");
    expect(code).toContain("case 'admin':");
    expect(code).toContain("default:");
    // break is omitted when arm body ends with return
    expect(code).not.toContain("break;");
  });

  it("should generate SET as variable assignment", () => {
    const mod = module({
      name: "Test",
      members: [
        action({
          name: "process",
          params: [],
          returnType: primitiveType("Void"),
          body: [set({ variable: "result", value: "42" })],
        }),
      ],
    });
    const code = generatePhp(mod);
    expect(code).toContain("$result = 42;");
  });

  it("should generate CALL as method call with $this->", () => {
    const mod = module({
      name: "Test",
      members: [
        action({
          name: "process",
          params: [],
          returnType: primitiveType("Void"),
          body: [call({ target: "doWork", args: ["a", "b"] })],
        }),
      ],
    });
    const code = generatePhp(mod);
    expect(code).toContain("$this->doWork($a, $b);");
  });

  it("should generate CALL with assignment", () => {
    const mod = module({
      name: "Test",
      members: [
        action({
          name: "process",
          params: [],
          returnType: primitiveType("Void"),
          body: [
            call({
              target: "doWork",
              args: ["a", "b"],
              assignTo: "result",
            }),
          ],
        }),
      ],
    });
    const code = generatePhp(mod);
    expect(code).toContain("$result = $this->doWork($a, $b);");
  });

  it("should generate FAIL as throw", () => {
    const mod = module({
      name: "Test",
      members: [
        action({
          name: "process",
          params: [],
          returnType: primitiveType("Void"),
          body: [
            fail({ error: "NotFound", message: "User not found" }),
          ],
        }),
      ],
    });
    const code = generatePhp(mod);
    expect(code).toContain(
      "throw new NotFoundException('User not found');",
    );
  });

  it("should generate FAIL without message", () => {
    const mod = module({
      name: "Test",
      members: [
        action({
          name: "process",
          params: [],
          returnType: primitiveType("Void"),
          body: [fail({ error: "NotFound" })],
        }),
      ],
    });
    const code = generatePhp(mod);
    expect(code).toContain("throw new NotFoundException();");
  });

  it("should generate EMIT as method call", () => {
    const mod = module({
      name: "Test",
      members: [
        action({
          name: "process",
          params: [],
          returnType: primitiveType("Void"),
          body: [
            emit({ event: "Created" }),
            emit({ event: "Updated", payload: "data" }),
          ],
        }),
      ],
    });
    const code = generatePhp(mod);
    expect(code).toContain("$this->emit('Created');");
    expect(code).toContain("$this->emit('Updated', $data);");
  });

  it("should generate RETRY as for loop with try/catch", () => {
    const mod = module({
      name: "Test",
      members: [
        action({
          name: "process",
          params: [],
          returnType: primitiveType("Void"),
          body: [
            retry({
              target: "fetchData",
              attempts: 3,
              delay: "1s",
              onExhaust: [
                fail({
                  error: "ServiceUnavailable",
                  message: "Service down",
                }),
              ],
            }),
          ],
        }),
      ],
    });
    const code = generatePhp(mod);
    expect(code).toContain(
      "for ($attempt = 1; $attempt <= 3; $attempt++)",
    );
    expect(code).toContain("try {");
    expect(code).toContain("$this->fetchData();");
    expect(code).toContain("break;");
    expect(code).toContain("} catch (\\Exception $e) {");
    expect(code).toContain("if ($attempt === 3)");
    expect(code).toContain("// delay: 1s");
  });

  it("should generate LIMIT as rate limit check", () => {
    const mod = module({
      name: "Test",
      members: [
        action({
          name: "api",
          params: [],
          returnType: primitiveType("Void"),
          body: [
            limit({
              rate: "100",
              per: "'minute'",
              onExceed: [
                fail({
                  error: "RateLimited",
                  message: "Too many requests",
                }),
              ],
            }),
          ],
        }),
      ],
    });
    const code = generatePhp(mod);
    expect(code).toContain("// LIMIT: 100 per");
    expect(code).toContain("if ($this->isRateLimitExceeded(100,");
    expect(code).toContain("throw new RateLimitedException");
  });

  it("should generate comments", () => {
    const mod = module({
      name: "Test",
      members: [
        action({
          name: "process",
          params: [],
          returnType: primitiveType("Void"),
          body: [comment({ text: "This is a comment" })],
        }),
      ],
    });
    const code = generatePhp(mod);
    expect(code).toContain("// This is a comment");
  });
});

