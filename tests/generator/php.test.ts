describe("PHP Generator: header", () => {
  it("should include PHP opening tag and header comment", () => {
    const mod = module({ name: "Empty", members: [] });
    const code = generatePhp(mod);
    expect(code).toContain("<?php");
    expect(code).toContain("// Generated by CodeSpec — do not edit manually");
    expect(code).toContain("declare(strict_types=1);");
  });

  it("should generate a class with the module name", () => {
    const mod = module({ name: "UserService", members: [] });
    const code = generatePhp(mod);
    expect(code).toContain("class UserService");
    expect(code).toContain("{");
    expect(code).toContain("}");
  });
});

