describe("PHP Generator: snapshot", () => {
  it("should produce deterministic output for a full module", () => {
    const mod = buildSampleModule();
    const code = generatePhp(mod);

    // Determinism check
    const code2 = generatePhp(mod);
    expect(code).toBe(code2);

    // Structure checks
    expect(code).toContain("<?php");
    expect(code).toContain("// Generated by CodeSpec — do not edit manually");
    expect(code).toContain("declare(strict_types=1);");
    expect(code).toContain("class UserRegistration");
    expect(code).toContain("public string $email;");
    expect(code).toContain("public ?string $nickname;");
    expect(code).toContain("public User $user;");
    expect(code).toContain(
      "class EmailAlreadyExistsException extends \\Exception",
    );
    expect(code).toContain(
      "public function register(string $email, string $password): User",
    );
    expect(code).toContain("return $user;");
  });

  it("should match snapshot", () => {
    const mod = buildSampleModule();
    const code = generatePhp(mod);
    expect(code).toMatchSnapshot();
  });
});
